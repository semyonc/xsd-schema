/// Emit the XSD-1.0 category `name` as a regex char-class *body* (what goes
/// INSIDE `[...]` or `[^...]`). Each codepoint is emitted as its literal
/// Unicode character; the five XSD char-class metacharacters
/// (`[`, `\\`, `]`, `-`, `^`) are escaped with a leading backslash so both
/// the Rust `regex` crate and `regexml` parse the output unambiguously.
/// Returns `None` for block names, typos, or intentionally unsupported
/// categories.
pub(crate) fn expand_xsd_category_body(name: &str) -> Option<&'static str> {
    static CACHE: OnceLock<HashMap<&'static str, String>> = OnceLock::new();
    let cache = CACHE.get_or_init(build_cache);
    cache.get(name).map(String::as_str)
}

/// Body for the XSD 1.0 `\w` escape under Unicode-3.0 pinning: the union
/// of the L, M, N, S category-group ranges (BMP-only). Per XSD 1.0 §F.1.2
/// `\w ::= [#x0-#x10FFFF]-[\p{P}\p{Z}\p{C}]`; pinning to Unicode 3.0 means
/// supplementary-plane and BMP-Cn codepoints are excluded as well — they
/// are unassigned under 3.0, so they fall outside L/M/N/S. The MS reU*
/// tests rely on this exclusion. Using the positive L+M+N+S form (instead
/// of `[^<P><Z><C>]`) keeps the class BMP-only without enumerating ranges.
pub(crate) fn xsd10_word_char_body() -> &'static str {
    static CACHE: OnceLock<String> = OnceLock::new();
    CACHE.get_or_init(|| {
        let mut s = String::with_capacity(16 * 1024);
        for cat in &["L", "M", "N", "S"] {
            if let Some(b) = expand_xsd_category_body(cat) {
                s.push_str(b);
            }
        }
        s
    })
}

/// Body for the XSD 1.0 `\W` escape under Unicode-3.0 pinning: the union
/// of the P, Z, C category-group ranges (BMP-only). Excludes supplementary
/// codepoints and BMP-Cn (both absent from our 3.0 P/Z/C tables) so that
/// MS reV*.i tests — which assert that supplementary codepoints match
/// neither `\w` nor `\W` under pre-3.1 / UTF-16-unit semantics — pass.
pub(crate) fn xsd10_non_word_char_body() -> &'static str {
    static CACHE: OnceLock<String> = OnceLock::new();
    CACHE.get_or_init(|| {
        let mut s = String::with_capacity(4 * 1024);
        for cat in &["P", "Z", "C"] {
            if let Some(b) = expand_xsd_category_body(cat) {
                s.push_str(b);
            }
        }
        s
    })
}

/// Body for the XSD 1.0 `\D` escape under Unicode-3.0 pinning: the Nd
/// ranges followed by the supplementary plane range (U+10000..U+10FFFD).
/// Used INSIDE a negated class — `[^<this>]` matches "BMP char not in Nd",
/// which simultaneously satisfies the MS reT.v tests (BMP-Cn like U+0BE6
/// must match `\D`) and reT.i tests (supplementary digits like U+1D7CE
/// must NOT match `\D`, because they are 2 surrogate units under the
/// UTF-16-unit semantics the tests were authored against).
pub(crate) fn xsd10_non_digit_neg_body() -> &'static str {
    static CACHE: OnceLock<String> = OnceLock::new();
    CACHE.get_or_init(|| {
        let mut s = String::new();
        if let Some(b) = expand_xsd_category_body("Nd") {
            s.push_str(b);
        }
        s.push_str("\u{10000}-\u{10FFFD}");
        s
    })
}

/// Body for the XSD 1.0 `\p{IsPrivateUse}` block under Unicode-3.0 pinning:
/// the BMP Private Use Area only (U+E000–U+F8FF). Modern regex engines
/// (including regexml) include the Plane 15 / Plane 16 supplementary PUAs
/// for backward-compatibility with the DIS XSD 1.1 block names, but those
/// blocks did not exist in Unicode 3.0 and the W3C MS reL/reM/reN tests
/// require them to be excluded under XSD 1.0.
pub(crate) fn xsd10_private_use_block_body() -> &'static str {
    "\u{E000}-\u{F8FF}"
}

fn build_cache() -> HashMap<&'static str, String> {
    let names: &[&str] = &[
        "Lu", "Ll", "Lt", "Lm", "Lo", "Mn", "Mc", "Me", "Nd", "Nl", "No", "Pc", "Pd", "Ps", "Pe",
        "Pi", "Pf", "Po", "Zs", "Zl", "Zp", "Sm", "Sc", "Sk", "So", "Cc", "Cf", "Co", "L", "M",
        "N", "P", "Z", "S", "C",
    ];
    let mut map = HashMap::with_capacity(names.len());
    for name in names {
        if let Some(ranges) = category_ranges(name) {
            let mut out = String::with_capacity(ranges.len() * 8);
            for &(s, e) in ranges {
                emit_range(&mut out, s, e);
            }
            map.insert(*name, out);
        }
    }
    map
}

fn push_char_class_char(out: &mut String, cp: u32) {
    // XSD char-class metacharacters (§G.4.1.3): `[` `\\` `]` `-` `^`.
    // `[` is escaped too so the regex parser does not treat a literal
    // `[` (e.g. from `\p{Ps}`) as opening a nested class.
    if cp == 0x5B || cp == 0x5C || cp == 0x5D || cp == 0x2D || cp == 0x5E {
        out.push('\\');
    }
    if let Some(c) = char::from_u32(cp) {
        out.push(c);
    }
}

fn emit_range(out: &mut String, s: u32, e: u32) {
    if s == e {
        push_char_class_char(out, s);
        return;
    }
    // If the range endpoint is `-` (U+002D), split so the dash is a
    // standalone escaped literal rather than a range operator.
    if s == 0x2D {
        push_char_class_char(out, 0x2D);
        if e > s + 1 {
            push_char_class_char(out, s + 1);
            out.push('-');
            push_char_class_char(out, e);
        } else {
            push_char_class_char(out, e);
        }
        return;
    }
    if e == 0x2D {
        if e > s + 1 {
            push_char_class_char(out, s);
            out.push('-');
            push_char_class_char(out, e - 1);
        } else {
            push_char_class_char(out, s);
        }
        push_char_class_char(out, 0x2D);
        return;
    }
    push_char_class_char(out, s);
    out.push('-');
    push_char_class_char(out, e);
}

#[cfg(test)]
mod tests {
    use super::*;

    fn in_ranges(rs: Ranges, cp: u32) -> bool {
        rs.iter().any(|&(s, e)| s <= cp && cp <= e)
    }

    #[test]
    fn lu_contains_ascii_uppercase() {
        assert!(in_ranges(LU_XSD10, 0x41));
        assert!(in_ranges(LU_XSD10, 0x5A));
        assert!(!in_ranges(LU_XSD10, 0x61));
    }

    #[test]
    fn lu_excludes_smp_math_alphanumerics() {
        // Added in Unicode 3.1; unassigned in 3.0. The reJ11.i test
        // relies on this exclusion.
        assert!(!in_ranges(LU_XSD10, 0x1D7A8));
    }

    #[test]
    fn co_stops_at_bmp_pua_end() {
        // Plane-15/16 PUA appeared in 3.1; 3.0 only has U+E000..U+F8FF.
        assert!(in_ranges(CO_XSD10, 0xE000));
        assert!(in_ranges(CO_XSD10, 0xF8FF));
        assert!(!in_ranges(CO_XSD10, 0x100000));
        assert!(!in_ranges(CO_XSD10, 0x10FFFD));
    }

    #[test]
    fn n_is_nd_nl_no_union() {
        for &(s, e) in ND_XSD10.iter().chain(NL_XSD10).chain(NO_XSD10) {
            for cp in s..=e {
                assert!(in_ranges(N_XSD10, cp), "cp U+{:04X} missing from N", cp);
            }
        }
    }

    #[test]
    fn expand_body_is_nonempty_for_known_cats() {
        for name in ["Lu", "Ll", "Lo", "Nd", "N", "L", "Cf", "Co", "M", "P", "S"] {
            let body = expand_xsd_category_body(name).expect(name);
            assert!(!body.is_empty(), "empty body for {}", name);
        }
    }

    #[test]
    fn expand_body_returns_none_for_unknown() {
        assert!(expand_xsd_category_body("IsBasicLatin").is_none());
        assert!(expand_xsd_category_body("Cn").is_none());
        assert!(expand_xsd_category_body("Cs").is_none());
        assert!(expand_xsd_category_body("Xx").is_none());
    }
}
